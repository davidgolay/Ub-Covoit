<h2>Sécurité</h2>
<div>Les renseignements personnels que nous collectons sont conservés dans un environnement sécurisé. 
Les personnes travaillant pour nous sont tenues de respecter la confidentialité de vos informations.</div>
<div><br></div>
<div>Pour assurer la sécurité de vos renseignements personnels, nous avons recours aux mesures suivantes :</div>
<div><br></div>
<div>
<ul>
<li>Sauvegarde informatique</li>
<li>Identifiant / mot de passe</li>
</ul>
</div>
<div>Nous nous engageons à maintenir un haut degré de confidentialité en intégrant les dernières innovations technologiques permettant d’assurer la confidentialité de vos transactions. 
Toutefois, comme aucun mécanisme n’offre une sécurité maximale, une part de risque est toujours présente lorsque l’on utilise Internet pour transmettre des renseignements personnels.</div>
